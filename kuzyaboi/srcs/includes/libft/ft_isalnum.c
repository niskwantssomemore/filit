#include "libft.h"

int		ft_isalnum(int c)
{
	int index;

	index = 0;
	if ((c >= '0' && c <= '9') || (c >= 95 && c <= 'z')
			|| (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
