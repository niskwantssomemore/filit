#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;

	str = (unsigned char *)s;
	while (n)
	{
		--n;
		if (*str == (unsigned char)c)
			return (str);
		if (n)
			str++;
	}
	return (0);
}
