#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		x;
	size_t	y;

	x = 0;
	while (s1[x])
		x++;
	y = 0;
	while (s2[y] && (y < n))
	{
		s1[x] = s2[y];
		x++;
		y++;
	}
	s1[x] = '\0';
	return (s1);
}
