#include "../includes/fillit.h"

static int	format(char *buf)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (buf[x] != '\0')
	{
		while (buf[x] != '\n' && buf[x] != '\0')
			x++;
		if (buf[x] == '\n')
		{
			x++;
			y++;
		}
		if (y == 4)
		{
			if (buf[x] != '\0' || buf[x] != '\n')
				return (0);
			if (buf[x] == '\n')
				x++;
			y = 0;
		}
	}
	return (1);
}
